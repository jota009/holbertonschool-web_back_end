#!/usr/bin/env python3
"""
3-to_str.py: Define a function that converts a float to its string form,
with full type annotations.
"""


def to_str(n: float) -> str:
    """
    Return the string representation of n.

    Args:
        n (float): The number to convert.

    Returns:
        str: Text form of n, same as str(n).
    """
    return str(n)
